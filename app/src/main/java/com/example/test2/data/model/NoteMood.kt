package com.example.test2.data.model

/**
 * 笔记心情枚举类型
 * 
 * 定义笔记可记录的各种心情状态
 */
enum class NoteMood {
    /**
     * 非常开心
     */
    VERY_HAPPY,
    
    /**
     * 开心
     */
    HAPPY,
    
    /**
     * 平静
     */
    NEUTRAL,
    
    /**
     * 难过
     */
    SAD,
    
    /**
     * 非常难过
     */
    VERY_SAD,
    
    /**
     * 疲惫
     */
    TIRED,
    
    /**
     * 沮丧
     */
    FRUSTRATED
} 